public class Calculator {


    public int adder(int numOne,int  numTwo){
        System.out.println(numOne+numTwo);
        return numOne + numTwo;
    }

    public int subtractor(int numOne,int  numTwo){
        System.out.println(numOne-numTwo);
        return numOne - numTwo;
    }

    public int multiplicanas(int numOne, int numTwo){
        System.out.println(numOne*numTwo);
        return numOne*numTwo;
    }
    public int divider(int numOne, int numTwo){
        System.out.println(numOne/numTwo);
        return numOne/numTwo;

    }
}
